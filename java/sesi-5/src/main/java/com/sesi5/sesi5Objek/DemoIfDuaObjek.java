package com.sesi5.sesi5Objek;

public class DemoIfDuaObjek {
  public void checkBilangan(int bilangan) {
    if (bilangan < 0) {
      System.out.println(bilangan + " merupakan bilangan NEGATIF.");
    } else if (bilangan == 0) {
      System.out.println("Nilai yang dimasukkan adalah NOL");
    } else {
      System.out.println(bilangan + " merupakan bilagan POSITIF.");
    }
  } 
}
